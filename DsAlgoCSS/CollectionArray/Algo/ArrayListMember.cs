             2.2.1 ArrayList 类的成员
        ArrayList 类包含几种用于 ArrayList 的方法和属性。下面这个列表就是最常用到的一些方法和属性：

        //属性
        Count：返回 ArrayList 中当前元素的数量。
        Capacity：存储 ArrayList 所能包含的元素的数量。        

        //方法
        Add()：向 ArrayList 添加一个元素。
        AddRange()： 在 ArrayList 末尾处添加群集的元素。
        Clear()：从 ArrayList 中移除全部元素。
        Contains()：确定制定的对象是否在 ArrayList 内。
        Copy To()：把 ArrayList 或其中的某一段复制给一个数组。
        GetEnumerator()：返回迭代 ArrayList 的计数器。
        GetRange()：返回 ArrayList 的子集作为 ArrayList。
        IndexOf()：返回指定数据项首次出现的索引。
        Insert()：在 ArrayList 的指定索引处插入一个元素。
        InsertRange()：从 ArrayList 指定索引处开始插入群集的元素。
        Item()：在指定索引处获取或者设置一个元素。
        Remove()：移除指定数据项的首次出现。
        RemoveAt()：在指定索引处移除一个元素。Reverse()：对 ArrayList 中元素的顺序进行反转。
        Sort()：对 ArrayList 中的元素按照阿拉伯字母表顺序进行排序。
        ToArray()：把 ArrayList 的元素复制给一个数组。
        TrimToSize()：为 ArrayList 中元素数量设置 ArrayList 的容量。

        */

