//埃拉托斯特尼筛法是快速筛选素数的算法，在处理大量整数是否是素数时有较高的效率。
//例：筛选小于n的整数并记录结果。
//解：计算sqrt(n)，∵对于任意的z<n，若z为合数，不妨设z = a*b，则必有min(a, b)<sqrt(n)
//	∴所有小于n的合数均可被小于sqrt(n)的整数整除
//	从2开始，依次去除小于n的整数中能被其整除的数，最后剩下的就是素数
