	void eratosthens(bool primelist[], int n) {
	int i, j, k;
	memset(primelist, true, sizeof(primelist));
	primelist[0] = primelist[1] = false;

	for (i = 2, k = 4; i <= n; i++, k += i + i - 1)
		if (primelist[i])
			for (j = k; j <= n; j += i)
				primelist[j] = false;
}